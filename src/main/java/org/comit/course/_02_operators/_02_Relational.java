package org.comit.course._02_operators;

public class _02_Relational {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 8;
		
		boolean equalsTo = a == b; // == comparison - returns a boolean
		boolean notEqualsTo = a != b;
		
		System.out.println("Equals To:" + equalsTo);
		System.out.println("Not equals to: " + notEqualsTo);

	}

}
